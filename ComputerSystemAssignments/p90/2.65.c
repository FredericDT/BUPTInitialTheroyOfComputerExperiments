#include<stdio.h>

int odd_ones(unsigned x) {
	x = x ^ (x >> 16);
	x = x ^ (x >> 8);
	x = x ^ (x >> 4);
	x = x ^ (x >> 2);
	return (x ^ (x >> 1)) & 1;
}

